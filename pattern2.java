package project2;

public class pattern2 {
	public static void main(String[] args)
	   {
	      int r=5;
	      
	      for(int i=0; i<r; i++)
	      {
	         int num=1;
	         for(int j=0; j<=i; j++)
	         {
	            System.out.print(num+ " ");
	            num++;
	         }
	         System.out.print("\n");
	      }
	   }
	}

